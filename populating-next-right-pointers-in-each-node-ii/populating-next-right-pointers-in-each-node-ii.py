class Solution:
    def connect(self, root: 'Node') -> 'Node':
        if not root:
            return
        # use queue to store nodes in every level, if queue[0] is not the last node, then let it point to the next node in the rest queue
        q = collections.deque()
        q.append(root)
        
        while q:
            size = len(q)
            for i in range(size):
                node = q.popleft()
                if i < size - 1:
                    node.next = q[0]
                if node.left:
                    q.append(node.left)
                if node.right:
                    q.append(node.right)
        return root